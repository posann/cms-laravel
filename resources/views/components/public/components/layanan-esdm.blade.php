<script>
    document.addEventListener('DOMContentLoaded', function() {
        const container = document.getElementById('infinite-scroll-container');
        const originalContent = document.getElementById('scroll-content');
        const duplicateContent = document.getElementById('scroll-content-duplicate');
        
        // Clone the original content
        duplicateContent.innerHTML = originalContent.innerHTML;
        
        let isDown = false;
        let startX;
        let scrollLeft;
        let animationId;

        
        // Mouse/Touch events for grabbing
        container.addEventListener('mousedown', (e) => {
            isDown = true;
            container.style.cursor = 'grabbing';
            startX = e.pageX - container.offsetLeft;
            scrollLeft = container.scrollLeft;
            cancelAnimationFrame(animationId);
        });
        
        container.addEventListener('mouseleave', () => {
            isDown = false;
            container.style.cursor = 'grab';
            autoScroll();
        });
        
        container.addEventListener('mouseup', () => {
            isDown = false;
            container.style.cursor = 'grab';
            autoScroll();
        });
        
        container.addEventListener('mousemove', (e) => {
            if (!isDown) return;
            e.preventDefault();
            const x = e.pageX - container.offsetLeft;
            const walk = (x - startX) * 2;
            container.scrollLeft = scrollLeft - walk;
        });
    });
</script>